package com.auguraclient.model;

public class Photo  extends AbstractModel {

	
	private String photoName;
	
	private String photoSmallPath;
	
	private String photoMiddlePath;
	
	private String photoBigPath;
	
	private String photoOriginPath;
	
	

	public String getPhotoName() {
		return photoName;
	}

	public void setPhotoName(String photoName) {
		this.photoName = photoName;
	}

	public String getPhotoSmallPath() {
		return photoSmallPath;
	}

	public void setPhotoSmallPath(String photoSmallPath) {
		this.photoSmallPath = photoSmallPath;
	}

	public String getPhotoMiddlePath() {
		return photoMiddlePath;
	}

	public void setPhotoMiddlePath(String photoMiddlePath) {
		this.photoMiddlePath = photoMiddlePath;
	}

	public String getPhotoBigPath() {
		return photoBigPath;
	}

	public void setPhotoBigPath(String photoBigPath) {
		this.photoBigPath = photoBigPath;
	}

	public String getPhotoOriginPath() {
		return photoOriginPath;
	}

	public void setPhotoOriginPath(String photoOriginPath) {
		this.photoOriginPath = photoOriginPath;
	}
	
	
	
	
}
